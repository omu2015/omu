package com.bit2015.omu.vo;

public class JusoVo {
	private String si;
	private String gun;
	private String dong;
	
	public String getSi() {
		return si;
	}
	public void setSi(String si) {
		this.si = si;
	}
	public String getGun() {
		return gun;
	}
	public void setGun(String gun) {
		this.gun = gun;
	}
	public String getDong() {
		return dong;
	}
	public void setDong(String dong) {
		this.dong = dong;
	}
	@Override
	public String toString() {
		return "JusoVo [si=" + si + ", gun=" + gun + ", dong=" + dong + "]";
	}
	
	

}
